/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
  /* Infinite loop */
    TWDT_DEF(startTask, 30000);
    TWDT_ADD(startTask);
    TWDT_CLEAR(startTask);
    
#if UART1_RECEVIE_BUFFER_SIZE > 0
    UART_PortConfig(1, &huart1, UART1_RECEVIE_BUFFER_SIZE);
#endif
#if UART2_RECEVIE_BUFFER_SIZE > 0
    UART_PortConfig(2, &huart2, UART2_RECEVIE_BUFFER_SIZE);
#endif
#if UART3_RECEVIE_BUFFER_SIZE > 0
    UART_PortConfig(3, &huart3, UART3_RECEVIE_BUFFER_SIZE);
#endif
#if UART4_RECEVIE_BUFFER_SIZE > 0
    UART_PortConfig(4, &huart4, UART4_RECEVIE_BUFFER_SIZE);
#endif

    /*初始化核心驱*/
    CMD_Init();
    System_Init();
    SFlash_Init(&hspi1);
#if RTC_EN > 0
    user_rtc_init();
#endif
    /*初始化系统功*/
    WorkParam_Init();
    //StartLog_Recoder();
    HTTP_Init();
    DFU_Init();
    DataSave_Init();
    Process_Init();
    /*初始化外设驱*/
#if NET_WIFI_EN > 0
    WIFI_Init();
#endif
#if NET_M2M_EN > 0
    M2M_Init();
#endif
#if NET_LAN_EN > 0
    w5500_Init(&hspi1);
#endif
    /*初始化业*/
#if PROCESS_EN > 0
    Process_Init();
#endif
#if MQTT_EN > 0
    MQTT_Conn_Init();
#endif
#if AES_EN > 0
    aes_init_ex();
#endif
    DBG_LOG("System Start!");
    LED_ALL_OFF();
    //UART_SetRemapping(4, 1);
    /* Infinite loop */
    for (;;) {
        osDelay(2);
        CMD_UART_Read_Poll();
        UART_Refresh_Poll();

        LED_FlashPoll();
        KEY_Poll();
        DFU_Poll();       
        TWDT_CLEAR(startTask);
        
    }

  /* USER CODE END 5 */ 
}

